    public boolean validarCampos() {
        return txtCpf.getText() != null
                && txtMatricula.getText() != null
                && txtNome.getText() != null
                && txtSalario.getText() != null
                && txtSenha.getText() != null;
    }

    public void limparCampos() {
        txtCpf.setText("");
        txtMatricula.setText("");
        txtNome.setText("");
        txtSalario.setText("");
        txtSenha.setText("");
    }

